    void eliminar (){
    int filaseleccionado=tabladatos.getSelectedRow();
    String Codigo_Persona=tabladatos.getValueAt(filaseleccionado,0).toString();
    if (filaseleccionado==-1){ // ese menos uno es de donde comienza la tabla comienza en -1
    JOptionPane.showMessageDialog(null,"debes seleccionar una fila");
    }else{
        String sql="delete from personas where Codigo_Persona="+Codigo_Persona;
        try {
            cn=con.getConnection();
            st=cn.createStatement();
            st.executeUpdate(sql);
            JOptionPane.showMessageDialog(null,"usuario eliminado");
            limpiartabla();
        } catch (Exception e) {
        }
    }
    }
    
